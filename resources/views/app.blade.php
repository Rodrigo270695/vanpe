        <link rel="icon" href="/pwa/icon-192x192.png" type="image/png" sizes="192x192">
        <link rel="icon" href="/pwa/icon-48x48.png" type="image/png" sizes="48x48">
        <link rel="apple-touch-icon" href="/pwa/apple-touch-icon.png" sizes="180x180">
        <meta name="theme-color" content="#1d6fd4">
        <meta name="application-name" content="VanPe">
        <meta name="apple-mobile-web-app-title" content="VanPe">
        <meta name="mobile-web-app-capable" content="yes">
        <meta name="apple-mobile-web-app-capable" content="yes">
        <meta name="apple-mobile-web-app-status-bar-style" content="default">

        @fonts

